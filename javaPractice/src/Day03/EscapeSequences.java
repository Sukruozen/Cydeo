package Day03;
/*
Will code some of the escape sequences
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("this is the; \n new line");
        System.out.println("this is a :\\backslash");
        System.out.println("This is a double quote: \"");
        System.out.println("How are you all today\nIt's nice to see you all\nWhat class do we have next week?");
        System.out.println("    *\n   * *\n  *   *\n *     *\n* * * * *");
        System.out.println("\tThis is a paragraph with tab at the beginning");

    }

}
